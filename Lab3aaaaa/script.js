// ЗАВДАННЯ 5: СТВОРИТИ ОБ'ЄКТ "СЕСІЯ" З УСПАДКУВАННЯМ ВІД "ГРУПА" ТА ДОДАТКОВИМИ МЕТОДАМИ ТА ВЛАСТИВОСТЯМИ
function runTask5() {
    // Об'єкт "Група" та методи, які успадковує "Сесія"
    var group = {
        Спеціальність: "",
        Шифр: "",
        "Кількість студентів": 0,
    };

    group.addInfo = function (specialty, faculty, numOfStudents) {
        this.Спеціальність = specialty;
        this.Шифр = faculty;
        this["Кількість студентів"] = numOfStudents;
    };

    group.editInfo = function (specialty, faculty, numOfStudents) {
        this.Спеціальність = specialty;
        this.Шифр = faculty;
        this["Кількість студентів"] = numOfStudents;
    };

    group.deleteInfo = function () {
        this.Спеціальність = "";
        this.Шифр = "";
        this["Кількість студентів"] = 0;
    };

    // Створення об'єкту "Сесія" з успадкуванням від "Група"
    var session = Object.create(group);
    session.Відраховано = 2;

    session.ВідсотокНеВідрахованих = function () {
        return ((this["Кількість студентів"] - this.Відраховано) / this["Кількість студентів"]) * 100;
    };

    // Перевизначення методу "Показати дані" в об'єкті "Сесія"
    session.showData = function () {
        alert("Дані сесії:\nСпеціальність: " + this.Спеціальність + "\nШифр: " + this.Шифр + "\nКількість студентів: " + this["Кількість студентів"] + "\nВідраховано: " + this.Відраховано + "\nВідсоток не відрахованих: " + this.ВідсотокНеВідрахованих() + "%");
    };

    // Використання методу "Показати дані" для об'єкту "Сесія"
    session.addInfo("Комп'ютерні науки", "ТР-25", 19);
    session.showData();
}

// ЗАВДАННЯ 6: РЕАЛІЗУВАТИ КЛАСИ "ГрупаКлас" І "СесіяКлас" З УСПАДКУВАННЯМ МЕТОДІВ ТА ВЛАСТИВОСТЕЙ
function runTask6() {
    // Клас "ГрупаКлас" з геттерами та сеттерами
    class ГрупаКлас {
        constructor() {
            this._спеціальність = "";
            this._шифр = "";
            this._кількістьСтудентів = 0;
        }

        get спеціальність() {
            return this._спеціальність;
        }

        set спеціальність(value) {
            this._спеціальність = value;
        }

        get ширф() {
            return this._шифр;
        }

        set ширф(value) {
            this._шифр = value;
        }

        get кількістьСтудентів() {
            return this._кількістьСтудентів;
        }

        set кількістьСтудентів(value) {
            this._кількістьСтудентів = value;
        }

        додатиДані(specialty, faculty, numOfStudents) {
            this.спеціальність = specialty;
            this.шифр = faculty;
            this.кількістьСтудентів = numOfStudents;
        }

        змінитиДані(specialty, faculty, numOfStudents) {
            this.спеціальність = specialty;
            this.шифр = faculty;
            this.кількістьСтудентів = numOfStudents;
        }

        видалитиДані() {
            this.спеціальність = "";
            this.шифр = "";
            this.кількістьСтудентів = 0;
        }
    }
    // Клас "СесіяКлас" успадковує "ГрупаКлас" і має додаткові методи та властивості
    class СесіяКлас extends ГрупаКлас {
        constructor() {
            super();
            this._відраховано = 0;
        }

        get відраховано() {
            return this._відраховано;
        }

        set відраховано(value) {
            this._відраховано = value;
        }

        відсотокНеВідрахованих() {
            return ((this.кількістьСтудентів - this.відраховано) / this.кількістьСтудентів) * 100;
        }
    }
    // Використання класів
    const група = new ГрупаКлас();
    група.додатиДані("Комп'ютерні науки", "ТР-25", 19);

    const сесія = new СесіяКлас();
    сесія.додатиДані("Комп'ютерні науки", "ТР-25", 19);
    сесія.відраховано = 2;

    // Виведення інформації
    alert("Дані групи:\nСпеціальність: " + група.спеціальність + "\nШифр: " + група.шифр + "\nКількість студентів: " + група.кількістьСтудентів);
    alert("Дані сесії:\nСпеціальність: " + сесія.спеціальність + "\nШифр: " + сесія.шифр + "\nКількість студентів: " + сесія.кількістьСтудентів + "\nВідраховано: " + сесія.відраховано + "\nВідсоток не відрахованих: " + сесія.відсотокНеВідрахованих() + "%");
}